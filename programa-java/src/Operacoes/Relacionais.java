package Operacoes;

public class Relacionais {

	public static void main(String[] args) {
		
		int nota1 = 10;
		int nota2 = 8;
		
		if(nota1 == nota2) {
			System.out.println("As notas são iguais");
		}else {
			System.out.println("As notas são diferentes");
		}
		
		/*
		 * OPERADOR    COMPARAÇÂO
		 *    ==          Igual  
		 *    !=       Diferente
		 *    <           Menor
		 *    >           Maior
		 *    <=       Menor Igual
		 *    >=       Maior Igual
		 */
	}
}
